CREATE OR REPLACE PROCEDURE TRABAJANDO_T1(SEGUNDOS IN NUMBER) AS

    ESTADO_ACTUAL NUMBER;
    ESTADO_ANTERIOR NUMBER;
    numeroT varchar(50);
    
BEGIN
    
    LOOP
        
        SELECT  SEQ_T1.NEXTVAL into ESTADO_ACTUAL FROM dual ; 
        
        IF  ESTADO_ACTUAL = ESTADO_ANTERIOR THEN EXIT;
        ELSE
   
            ESTADO_ANTERIOR := ESTADO_ACTUAL;
            DORMIR(5);
   
        END IF;
   
    END LOOP;
  
   SELECT dbms_transaction.local_transaction_id into numeroT FROM dual;
    
    DBMS_OUTPUT.PUT_LINE('SE HA DORMIDO LA TRANSACCION CON ID: ' || numeroT);
    DBMS_OUTPUT.PUT_LINE('TRABAJANDO_T1: HE TERMINADO DE TRABAJAR.');

END;
